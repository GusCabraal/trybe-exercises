const num1 = 10;
const num2 = 5;

function somarNumeros(a, b) {
    let result;
    result = a + b;
    return result;
}

function subNumeros(a, b) {
    let result;
    result = a - b;
    return result;
}

function multNumeros(a, b) {
    let result;
    result = a * b;
    return result;
}

function divNumeros(a, b) {
    let result;
    result = a / b;
    return result;
}

function modNumeros(a, b) {
    let result;
    result = a % b;
    return result;
}


console.log(somarNumeros(num1, num2))
