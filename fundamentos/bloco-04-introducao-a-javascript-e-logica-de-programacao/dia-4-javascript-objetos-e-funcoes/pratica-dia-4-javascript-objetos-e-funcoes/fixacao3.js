const num1 = 10;
const num2 = 5;

function somarNumeros(a, b) {
    return a + b;
}

function subNumeros(a, b) {
    return a - b;
}

function multNumeros(a, b) {
    return a * b;
}

function divNumeros(a, b) {
    return a / b;
}

function modNumeros(a, b) {
    return a % b;
}


console.log(somarNumeros(num1, num2))
console.log(subNumeros(num1, num2))
console.log(multNumeros(num1, num2))
console.log(divNumeros(num1, num2))
