// Faça um programa que, dado um valor definido numa constante, retorne "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.

const valor = 10;

if (valor >= 90) {
    console.log("A")
}
else if (valor >= 80){
    console.log("B")
}
else if (valor >= 70){
    console.log("C")
}
else if (valor >= 60){
    console.log("D")
}
else if (valor >= 50){
    console.log("E")
} else {
    console.log("F")
}